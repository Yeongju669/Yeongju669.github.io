//Q

function q() {
  if($('.q').hasClass('do')) {
    $('.q').removeClass('do');
    $('.q').addClass('upp');
  }else {
    $('.q').removeClass('upp');
    $('.q').addClass('do');
  }}

  function animateQ(e) {
      if(e.key === 'q') {
        q();
      }
  }
  $('.q').click(q);
  $(window).keyup(animateQ);


